CREATE TABLE `Projeto` (
  `id` bigint(20) NOT NULL AUTO_INCREMENT,
  `portal_id` bigint(20) DEFAULT NULL,
  `nome` varchar(255) DEFAULT NULL,
  `descricao` varchar(255) DEFAULT NULL,
  `nome_backend_app` varchar(255) DEFAULT NULL,
  `nome_front_end_app` varchar(255) DEFAULT NULL,
  `icone_app` varchar(255) DEFAULT NULL,
  `image_app` varchar(255) DEFAULT NULL,
  `server_lang` varchar(255) DEFAULT NULL,
  `databse_flavor` varchar(255) DEFAULT NULL,
  `server_host` varchar(255) DEFAULT NULL,
  `server_port` bigint(20) DEFAULT NULL,
  `front_host` varchar(255) DEFAULT NULL,
  `front_port` bigint(20) DEFAULT NULL,
  `use_login`  bit(1) DEFAULT NULL,
  `use_roles`  bit(1) DEFAULT NULL,
  `output_directory` varchar(255) DEFAULT NULL,
  PRIMARY KEY (`id`),
) ENGINE=InnoDB  DEFAULT CHARSET=UTF8MB4;
