package org.idomine.domain.crud.model;

import java.util.List;
import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;


import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.EqualsAndHashCode;
import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;
import lombok.ToString;


@Entity
@Getter
@Setter
@Builder
@AllArgsConstructor
@NoArgsConstructor
@ToString
@EqualsAndHashCode(of = { "id" })
public class Formly
{
    @Id
    @GeneratedValue
    private Long id;
    
    private String campoText;
    
    private String campoTextArea;
    
    @Temporal(TemporalType.DATE)
    private Date campoData;
    
    private Boolean campoCheckbox;
    
    private String campoSelectOne;
    
    private String campoRadioGroup;
    
    private String campoToggle;
    
    
}
